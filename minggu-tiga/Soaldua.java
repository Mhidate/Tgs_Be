public class Soaldua {
  public static void main(String[] args) {
    //r jari-jari
   // rumus Luas lingkaran = π × r²
    
   //variabel untuk luas lingkaran
    double r = 2.5, phi = 3.14 , luaslingkaran = phi * r * r;
    
    
   // rumus volume balok = panjang * lebar * tinggi 
    
    //variabel untuk volume balok
    double p = 6, l = 3, t = 4, volume = p*l*t; 

    System.out.println("--------------------------------------------------------");
    System.out.println("Soal no 2 luas lingkaran");
    System.out.println();
    System.out.println("Rumus Luas lingkaran = π * r²");
    System.out.println(" π (phi) = " + phi);
    System.out.println(" r = " + r);
    System.out.println(" Luas Lingkaran = "+luaslingkaran);
    System.out.println("--------------------------------------------------------");
    System.out.println();
    System.out.println("--------------------------------------------------------");
    System.out.println("Soal no 2 volume balok");
    System.out.println();
    System.out.println("Rumus volume balok = panjang * lebar * tinggi ");
    System.out.println(" Panjang = " + p);
    System.out.println(" Lebar = " + l);
    System.out.println(" Tinggi = " + t);
    System.out.println(" Volume balok = " + volume );
    System.out.println("--------------------------------------------------------");
  }
}
